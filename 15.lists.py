names = ['bilal', 'zahid', 'umer', 'obaid', 'hamza']
print("----------------------Indexed Operations------------------")
print(f'''
print(names) => {names}
print(names[0]) => {names[0]}
print(names[0:3]) => {names[0:3]}
print(names[:]) => {names[:]}
''')
print("----------------Modify & Update The List-----------------------")
names[0] = 'Bilal Khan'
print(f"names[0] = 'Bilal Khan' => {names}")